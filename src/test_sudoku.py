import unittest
from typing import Tuple

import numpy as np

from .sudoku import sudoku_solver


class MyTestCase(unittest.TestCase):

    def test_sudoku_solver(self):
        input, expect_output = self.init_data()
        output = sudoku_solver(input.copy())
        print(input)
        print(output)
        print(expect_output)
        np.testing.assert_array_equal(expect_output, output)

    @staticmethod
    def init_data() -> Tuple:
        # https://en.wikipedia.org/wiki/Sudoku
        in_data: np.ndarray = np.array([
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],

            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],

            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9],
        ])

        out_data: np.ndarray = np.array([
            [5, 3, 4, 6, 7, 8, 9, 1, 2],
            [6, 7, 2, 1, 9, 5, 3, 4, 8],
            [1, 9, 8, 3, 4, 2, 5, 6, 7],

            [8, 5, 9, 7, 6, 1, 4, 2, 3],
            [4, 2, 6, 8, 5, 3, 7, 9, 1],
            [7, 1, 3, 9, 2, 4, 8, 5, 6],

            [9, 6, 1, 5, 3, 7, 2, 8, 4],
            [2, 8, 7, 4, 1, 9, 6, 3, 5],
            [3, 4, 5, 2, 8, 6, 1, 7, 9],
        ])
        return in_data, out_data


if __name__ == '__main__':
    unittest.main()
